<h3><font face="courier" color="aqua">ABOUT THIS GAME:</font></h3>
<p><font face="courier" color="aqua">Disables timers as long as your squad is concealed.</font></p>

<p><font face="courier" color="aqua">*NOTE: If disabled, you will not be able to load any campaigns that had used this mod until reenabled*<br> 
*This mod may work with other mods that extend or modify timer durations.*</font></p> 

<p><font face="courier" color="aqua">This mod seeks a nice middle area between outright removing the timers and extending them.</font><p> 

<p><font face="courier" color="aqua">As long as your squad remains concealed, timers will not tick down.</font></p>

<h3><font face="courier" color="aqua">INTALLATION</font></h3> 

