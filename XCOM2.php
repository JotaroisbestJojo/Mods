<h1><font face="courier" color="aqua">1.)Long War 2</font></h1>
<img src="633-0-1484839888.jpg" width="400" height="300">
<h3><font face="courier" color="aqua">ABOUT THIS MOD:</font></h3>
<p><font face="courier" color="aqua">Long War 2 is a significant overhaul of XCOM 2 aimed at giving players the feel of running a worldwide guerrilla war against ADVENT and offering them a greater variety of strategic and tactical experiences.</font></p>
<p><font face="courier" color="aqua">Long War 2 by Pavonis Interactive (formerly Long War Studios)</font></p>

<p><font face="courier" color="aqua">Long War 2 is a significant overhaul of XCOM 2 aimed at giving players the feel of running a worldwide guerrilla war against ADVENT and offering them a greater variety of strategic and tactical experiences.</font></p>

<p><font face="courier" color="aqua">Many of the mod's new mechanics are described ingame in the XCOM archives. Players are strongly encouraged to read those entries immediately after finishing Gatecrasher.</font></p>

<p><font face="courier" color="aqua">Features include:</font></p>
<p><font face="courier" color="aqua">1.) A much longer campaign, running for 100 to 120 missions on average</font></p>
<p><font face="courier" color="aqua">2.) Infiltration mechanics that require you to send out multiple squads at once</font></p>
<p><font face="courier" color="aqua">3.) Manage resistance Havens and have resistance members scrounge for supplies, gather intelligence or recruit more people to your cause</font></p>
<p><font face="courier" color="aqua">4.) Nine soldier classes: Sharpshooter, Shinobi, Ranger, Assault, Gunner, Grenadier, Specialist, Technical and Psionic (plus Sparks for DLC owners), each with unique secondary weapons</font></p>
<p><font face="courier" color="aqua">5.) Two new weapon tiers, lasers and coilguns</font></p>
<p><font face="courier" color="aqua">6.) Dozens of new enemies and smarter battlefield AI</font></p>
<p><font face="courier" color="aqua">7.) ADVENT strategic AI that tries to counter your moves</font></p>

<h3><font face="courier" color="aqua">COMPATIBILITY</font></h3> 

<p><font face="courier" color="aqua">This is a "total conversion" of XCOM2 and will not work with other total conversions. It does include a new XComGame.upk.</font></p>

<p><font face="courier" color="aqua">Long War Studios' / Pavonis Interactive's mods SMGPack, AlienPack, LeaderPack, Toolbox, PerkPack, and LaserPack are already integrated into this mod (with different settings and functionality) and should NOT be activated when playing campaigns.</font></p> 

<p><font face="courier" color="aqua">Other mods may not necessarily work given the depth of changes.</font></p> 

<h3><font face="courier" color="aqua">TROUBLESHOOTING</font></h3> 

<p><font face="courier" color="aqua">If you are have problems with the mod, you should first exit the game and REGENERATE YOUR INI files by deleting all files in \Documents\My Games\XCOM2\XComGame\Config. These will be recreated on your next game start. If that does not resolve your issues, you may need to verify XCOM2 back to its vanilla state in Steam before activating this and other mods.</font></p>

<p><font face="courier" color="aqua">An additional temporary workaround to crashes when starting a new campaign is to move all of your old save files out of \Documents\My Games\XCOM2\XComGame\SaveData.</font></p> 

<h3><font face="courier" color="aqua">INSTALLATION</font></h3>

<p><font face="courier" color="aqua">Manually Installing the mod:</font></p>
<p><font face="courier" color="aqua">After unzipping, Copy the contents of the mod package to the directory \Steam\steamapps\common\XCOM 2\XComGame\Mods\</font></p>

<p><font face="courier" color="aqua">Manually Uninstalling the mod</font></p>
<p><font face="courier" color="aqua">Remove the folder \Steam\steamapps\common\XCOM 2\XComGame\Mods\LW_Overhaul\</font></p>

