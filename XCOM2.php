<h3><font face="courier" color="aqua">ABOUT THIS MOD:</font></h3>
<p><font face="courier" color="aqua">Adds several japanese inspired swords as alternatives to the game sword.</font></p>

<p><font face="courier" color="aqua">Adds several new weapons with japanese influence. 3 tiers of katanas and 3 tiers of wakizashis, corresponding with the game swords.</font></p>

<p><font face="courier" color="aqua">The mod adds the swords to a savegame, just enable the mod and you will be able to equip them. There is no need to investigate it or buy it, its added by default. Remember, removing the mod will invalidate the savegames where it has been used.</font></p>

<p><font face="courier" color="aqua">To install it manually, Unzip the mod into the (XCOMDirectory)\XComGame\Mods then enable it on the mod launcher.</font></p>

<p><font face="courier" color="aqua">Sword stats are now on the configuration file, so you can set the weapons as strong or weak as you like. You edit the sword stats by editing the file XComGame.ini at (XCOMDirectory)\XComGame\Mods\KatanaMod\Config</font></p>
